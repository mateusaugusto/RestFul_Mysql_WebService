package dto;

public class FeedObjectsChat {
	
	private String id;
	private String nome;
	private String msg;
	private String id_festa;
	
	
	
	public String getId_festa() {
		return id_festa;
	}
	public void setId_festa(String id_festa) {
		this.id_festa = id_festa;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}

	
	
	
}
